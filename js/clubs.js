function pack(list, division) {
  return list.map((row, i) => {
    const [id, name, short, power, money] = row;
    const theme = CLUB_KIT_THEMES[id];
    return {
      id,
      name,
      short,
      power,
      money,
      color: theme?.primary || COLORS[i % COLORS.length],
      division,
    };
  });
}

